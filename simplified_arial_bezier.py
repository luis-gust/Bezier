    def show_alphabet(self) -> plt.Figure:
        fig, axes = plt.subplots(5, 6, figsize=(24, 20))
        fig.suptitle('',
                    fontsize=16, fontweight='bold')
        
        letters = list("ABCDEFGHIJKLMNOPQRSTUVWXYZ")
        colors = ['#1f77b4', '#ff7f0e', '#2ca02c', '#d62728']
        
        for i, letter_name in enumerate(letters):
            row = i // 6
            col = i % 6
            ax = axes[row, col]
            
            if letter_name in self.letters:
                letter = self.letters[letter_name]
                
                for j, curve in enumerate(letter.curves):
                    points = curve.get_points(50)
                    ax.plot(points[:, 0], points[:, 1], 
                           color=colors[j % len(colors)], linewidth=4,
                           solid_capstyle='round', solid_joinstyle='round')
            
            ax.set_aspect('equal')
            ax.set_xlim(20, 280)
            ax.set_ylim(60, 500)
            ax.set_title(f'{letter_name}', fontsize=16, fontweight='bold')
            ax.grid(True, alpha=0.15)
            ax.set_xticks([])
            ax.set_yticks([])
        
        for i in range(26, 30):
            row = i // 6
            col = i % 6
            axes[row, col].set_visible(False)
        
        plt.tight_layout()
        return fig

